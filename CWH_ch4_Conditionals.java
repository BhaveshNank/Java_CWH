public class CWH_ch4_Conditionals {

    public static void main(String[] args) {
        int age = 19;
        if (age>18) {
            System.out.println("You can drive");
        }
        else{
            System.out.println("You cannot drive");
        }
    }
}
// note its not necessary to have both if and else in conditional statement your program 
// will still work if there is only if statement present in your program 